package day2.loop;
public class Example3 {

	public static void main(String[] args) {
		for (int i=0; i<5; i+=1) {
			System.out.println(i);
		}
		
		System.out.println("Printing even numbers");
		for (int i=0; i<=10; i+=2) {
			System.out.println(i);
		}
		
		System.out.println("Printing odd numbers-");
		for(int i=1; i<=10; i+=2) {
			System.out.println(i);
		}
	}
}
